  public static void gameOfLife(int[][] gridM) {

    // array for 8 neighboring cells for a  cell
    int rows[] = {1, -1, 0, 1, -1, 0, 1, -1};
    int cols[] = {0, 0, -1, -1, -1, 1, 1, 1};

    for (int row = 0; row < m; row++) {
      for (int col = 0; col < n; col++) {
        int countLivesNeighBors = 0;

        for (int index = 0; index < 8; index++) {
          int rNeighbor = (row + rows[index]);
          int cNeighbor = (col + cols[index]);
          if (isValid(gridM, rNeighbor, cNeighbor)) {
            countLivesNeighBors += 1;
          }
        }

        // Rule 1 or Rule 3
        if ((gridM[row][col] == 1) && (countLivesNeighBors < 2 || countLivesNeighBors > 3)) {

          gridM[row][col] = -1; //now dead but originally was live
        }
        // Rule 4
        if (gridM[row][col] == 0 && countLivesNeighBors == 3) {
          //
          gridM[row][col] = 2; //now live but was originally dead.
        }
      }
    }

    // representation of updated gridM.
    for (int row = 0; row < m; row++) {
      for (int col = 0; col < n; col++) {
        if (gridM[row][col] > 0) {
          gridM[row][col] = 1;
          System.out.print("* ");
        } else {
          gridM[row][col] = 0;
          System.out.print(". ");
        }
      }
      System.out.println();
    }

  }

